/**
 * Field slugs the legacy Vue exporter dropped before forwarding the column
 * list to `query-export`. They're either internal bookkeeping (parent IDs,
 * sort_order, â€¦) or duplicates of presentation metadata (`icon`, `color`).
 */
/*
 * Decode HTML entities in strings returned by the backend (e.g. `&amp;` â†’ `&`).
 * Uses a temporary textarea so the browser's HTML parser does the work â€” safe
 * in all client-only contexts (SSR would need a library like `he`).
 */
function decodeHtmlEntities(str: string): string {
  const txt = document.createElement('textarea')

  txt.innerHTML = str

  return txt.value
}

