/**
 * Shape of a TanStack DB collection generated by `@docyrus/tanstack-db-generator`.
 * Only `list` is required by this hook; consumers may pass the full collection.
 */
export interface DocyrusDataGridCollection<TData> {
  list: (
    params?: Record<string, unknown>,
  ) => Promise<Array<TData> | { data: Array<TData> }>
  /** PATCH `/items/bulk` — used for inline change save and bulk-update dialog. */
  updateMany?: (body: {
    records: Array<{ id: string } & Record<string, unknown>>
    enableAutomation?: boolean
    enableChangeLogging?: boolean
  }) => Promise<unknown>
  /** DELETE `/items` — used for bulk delete from the selection toolbar. */
  deleteMany?: (body: {
    recordIds: Array<string>
    childActions?: Record<string, 'keep' | 'delete' | 'user_decides'>
  }) => Promise<unknown>
}

/** Built-in bulk actions surfaced when one or more rows are selected. */
